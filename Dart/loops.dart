void main() {
  // for loop
  for (int i = 1; i<6; i++) {
    print('this iteration is $i');
  }
  
  
  // while loop
  
  int j = 1;
  while (j < 6) {
    print('this ittration in while loop is $j');
    j++;
  }
  

  // do-while loop
  int k = 1;
  do {
    print('This ittration in do-while loop us $k');
    k++;
  } while (k < 6);
}